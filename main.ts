function updateSpeed () {
    if (info.score() < 15) {
        gameSpeed = 15
    } else {
        gameSpeed = info.score()
    }
    if (Math.percentChance(25)) {
        enemySpeed = gameSpeed
    } else {
        enemySpeed = gameSpeed / 3
    }
    if (boost > 0) {
        aPet.startEffect(effects.fire)
        boost = boost - 1
        petSpeed = gameSpeed * 2
    } else {
        petSpeed = gameSpeed
        effects.clearParticles(aPet)
    }
}
